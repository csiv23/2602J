task Straighten
{
	int RightValue;
	int LeftValue;
	int difference;

	RightValue = SensorValue[RightEncoder];
	LeftValue = SensorValue[LeftEncoder];
	difference = (SensorValue[RightEncoder] - SensorValue[LeftEncoder]);

	if(difference > 0)
	{
		motor[rightSideDrive] = (127 - 10);
		motor[leftSideDrive] = 88;
	}

	if(difference < 0)
	{
		motor[leftSideDrive] = (88-10);
		motor[rightSideDrive] = 127;
	}

	if(difference == 0)
	{
		motor[leftSideDrive] = 88;
		motor[rightSideDrive] = 127;
	}

}
